#!/bin/bash

####Common Escape Sequences####
#  Newline (\n): Adds a new line.
#  Tab (\t): Adds a horizontal tab.
#  Vertical Tab (\v): Adds a vertical tab.
#  Backslash (\\): Escapes the next character.

echo "############### Demonstrating Tab, Vertical Tab, and Newline Characters ###############"
echo

# Horizontal Tab
echo -e "Horizontal Tab Example:\nThis is\t  Deepak\t  Kesarkar"
echo

# Vertical Tab
echo -e "Vertical Tab Example:\nThis is\vDeepak\vKesarkar"
echo

# New Line
echo -e "New Line Example:\nThis is\nDeepak\nKesarkar"

